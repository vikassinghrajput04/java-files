package com.student.example;

public class Student {

	private String Name;
	private String DressCode;
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getDressCode() {
		return DressCode;
	}
	public void setDressCode(String dressCode) {
		DressCode = dressCode;
	}
}
